package TP3;

public class AreaEmpresa {
	private String area;
	private int tiempo;
	
	public AreaEmpresa(String area, int tiempo) {
		this.area=area;
		this.tiempo=tiempo;
	}
	
	public String getArea() {
		return area;
	}
	public void setArea(String area) {
		this.area = area;
	}
	public int getTiempo() {
		return tiempo;
	}
	public void setTiempo(int tiempo) {
		this.tiempo = tiempo;
	}
	
	
}
